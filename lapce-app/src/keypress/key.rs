    fn mouse_from_str(s: &str) -> Option<floem::glazier::PointerButton> {
        use floem::glazier::PointerButton as B;

        Some(match s {
            "mousemiddle" => B::Auxiliary,
            "mouseforward" => B::X2,
            "mousebackward" => B::X1,
            _ => return None,
        })
    }
